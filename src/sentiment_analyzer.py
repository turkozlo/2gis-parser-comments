"""
Модуль для анализа тональности отзывов через Mistral API
"""
import json
from pathlib import Path
from langchain_mistralai import ChatMistralAI
from langchain_core.messages import HumanMessage, SystemMessage

# Загрузка конфигурации
CONFIG_PATH = Path(__file__).parent.parent / "config.json"

def load_config():
    """Загружает конфигурацию из config.json"""
    with open(CONFIG_PATH, "r", encoding="utf-8") as f:
        return json.load(f)

# Инициализация клиента Mistral
config = load_config()
client = ChatMistralAI(
    model_name=config.get("mistral_model", "mistral-small-latest"),
    api_key=config["mistral_api_key"]
)

# Системный промпт для тегирования отзывов
SYSTEM_PROMPT = """Ты - эксперт по анализу отзывов клиентов банка.

ГЛАВНАЯ ЗАДАЧА: Найти ПРОБЛЕМЫ и ЖАЛОБЫ для их устранения!

КРИТИЧЕСКИ ВАЖНО! Теги работают по принципу ИЛИ-ИЛИ:

1. Если в отзыве есть ХОТЬ ОДНА проблема/жалоба/негатив (даже если есть и позитив):
   → Ставь ТОЛЬКО теги проблем из списка ниже
   → НЕ СТАВЬ #Позитивное/без_комментария

2. Если отзыв ПОЛНОСТЬЮ позитивный или пустой (НЕТ НИКАКИХ проблем):
   → Ставь ТОЛЬКО: #Позитивное/без_комментария

ТЕГИ ПРОБЛЕМ (для негатива):
#Обслуживание - очереди, грубость, медленное обслуживание, некомпетентность сотрудников
#Состояние_офиса - грязь, духота, холод, сломанная мебель, плохое освещение
#Инфраструктура - плохая парковка, неудобное расположение, нет пандуса
#Технические_проблемы - сломанные банкоматы, не работает оборудование
#Проблема_с_продуктом_банка - проблемы с картой, приложением, переводами, кредитами
#Фрод/Навязывание_услуг - навязывание, обман, мошенничество
#Прочее - другие проблемы

Правила:
- Выбирай ВСЕ подходящие теги проблем
- #Позитивное/без_комментария и теги проблем НИКОГДА не должны быть вместе!
- Приоритет: если есть проблема - игнорируй позитив
- Отвечай ТОЛЬКО тегами через запятую
- Никаких объяснений

Примеры:

Отзыв: "Спасибо, всё отлично!"
Ответ: #Позитивное/без_комментария

Отзыв: ""
Ответ: #Позитивное/без_комментария

Отзыв: "Хороший офис! Сотрудники отзывчивые! Но очереди..."
Ответ: #Обслуживание

Отзыв: "Персонал молодцы, но банкоматы не работают круглосуточно"
Ответ: #Технические_проблемы

Отзыв: "Всё понравилось, быстро обслужили. Парковка ужасная."
Ответ: #Инфраструктура

Отзыв: "Норм, быстро. Минус - нет опознавательных знаков"
Ответ: #Инфраструктура

Отзыв: "Всё отлично! Просьба перенести постамат"
Ответ: #Инфраструктура

Отзыв: "Долго ждал, грязно, холодно"
Ответ: #Обслуживание, #Состояние_офиса"""

def analyze_tags(review_text: str) -> str:
    """
    Определяет подходящие теги для отзыва через Mistral API
    
    Логика:
    - Если есть ХОТЬ ОДНА проблема → теги проблем (БЕЗ #Позитивное/без_комментария)
    - Если НЕТ проблем → #Позитивное/без_комментария
    
    Args:
        review_text: Текст отзыва
        
    Returns:
        Строка с тегами через запятую
    """
    if not review_text or not review_text.strip():
        return "#Позитивное/без_комментария"
    
    try:
        messages = [
            SystemMessage(content=SYSTEM_PROMPT),
            HumanMessage(content=f"Отзыв: {review_text}")
        ]
        
        response = client.invoke(messages)
        result = response.content.strip()
        
        # Проверка что ответ содержит хотя бы один тег с решеткой
        if "#" in result:
            return result
        else:
            return "#Позитивное/без_комментария"
            
    except Exception as e:
        print(f"Ошибка при анализе отзыва: {e}", flush=True)
        return "#Прочее"
